    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        Cliente novo = this.dados.buscarCliente(txtInCPF.getText());
        if(novo!=null){
            txtNome.setText(novo.getNome());
            txtTel.setText(novo.getTelefone());
            txtCPF.setText(novo.getCPF());
            txtRua.setText(novo.getEndereco().getRua());
            txtNum.setText(novo.getEndereco().getNum());
            txtBairro.setText(novo.getEndereco().getBairro());
            txtCidade.setText(novo.getEndereco().getCidade());
            
            for(ReservaDeViagem re: novo.getReservasViagens()){
                model.addElement(re.getNumDeViagem());
            }
            listaReservas.setModel(model);
        }
        else{
            JOptionPane.showMessageDialog(null,"Cliente n√£o encontrado!","Erro",0);   
        }
    }//GEN-LAST:event_buscarActionPerformed

